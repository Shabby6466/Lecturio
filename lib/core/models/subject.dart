import 'package:hive/hive.dart';

part 'subject.g.dart';

@HiveType(typeId: 0)
class Subject extends HiveObject {
  @HiveField(0)
  final String id;

  @HiveField(1)
  final String name;

  @HiveField(2)
  final String icon; // Icon name from font_awesome or material

  @HiveField(3)
  final String color; // Hex string

  Subject({
    required this.id,
    required this.name,
    required this.icon,
    required this.color,
  });
}
